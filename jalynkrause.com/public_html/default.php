									<h2>Astronomy</h2>
								</header>
								<p>
								    My heart lies with astronomy and the infinite unanswered questions warped beyond our horizons. As an undergraduate student at the University of Wisconsin - Madison, I was involved in a few research projects within UW's Astronomy-Physics Department focused on galaxy evolution.
								    <br>
                                    <br> 
                                    Explore my findings and collaborations below!
								</p>
								<section>
									<div class="row">
                                        <h4>Maneuvering around the Dust and Gas Remnants of Zwicky 049.057</h4>
                                        <a href="#" class="image fit"><img src="images/zw049.jpg" alt=""></a>
                                        <p>
                                            Above is a color composite image by Dr. Ralf Kotulla of Zwicky 049.057 viewed from the Hubble Space Telescope Wide Field Camera 3. This is the first of many galaxies we performed image analysis on as a team. Did you notice the massive outflow jet from its center?
                                            <br>
                                            <br>
                                            Since May of 2019, I've had the joy of working alongside Professor Dr. John (Jay) S. Gallagher, III and his research group to explore the high-density dust regions of compact galactic centers with outflow jets. After careful photometric measurements were made, we reduced long-slit spectroscopic data from the South African Large Telescope (SALT) using astronomical software packages found in IRAF, Python, Mathematica, SAOImageDS9, and more. 
                                            <br>
                                            <br>
                                            Throughout this time, IRAF and I have especially become best friends. Together, we have reduced SALT RSS long-slit data for over 15 galaxies, plus fit and analyzed their spectra. My most exhausted resources for reducing SALT longslit data can be found <a href='http://mips.as.arizona.edu/~khainline/salt_redux.html'>here</a> and <a href='http://mips.as.arizona.edu/~khainline/salt_redux.html'>here</a>. Enjoy! I am in progress of making my own IRAF reduction manual -- reach out to my email if you want tips and tricks until then!
                                            <br>
                                            <br>
                                            In the summer of 2020, I participated in UW-Madison's Astrophysics REU program under Prof. Gallagher's mentorship. Virtually, we continued the work we began in the spring of the year prior. During the 10-week program, I focused on engineering statistical interstellar dust and extinction models for Zw049.057. This was when I began learning Linux.
                                            <br>
                                            <br>
                                            All this work is currrently in progress and in the process of publication! Stay tuned...
                                        </p>
                                       
									</div>
									<div class="row">
                                        <h4>Investigating the origins of oxygen abundance gradient anomalies with MaNGA</h4>
                                        <a href="#" class="image fit"><img src="images/gals.jpg" alt=""></a>
                                        
                                        <p>
                                            Our goal is to examine the chemical evolution in over 800 disk galaxies from the Sloan Digital Sky Survey-IV MaNGA survey to further understand galaxy evolution at high redshifts. 
                                            <br>
                                            <br>
                                            Some astrophysics backgroud: as interstellar gas cools and collapses within a galaxy, new stars form and evolve, ultimately ejecting chemical elements back into the gas surrounding a galaxy when they explode as supernova. This process creates the inward to outward trend of metallicity abundances we are interested in studying. 
                                            <br>
                                            <br>
                                            To further our understandings, we created an autonomous classification process in the Python computing language to analyze changes in the radial metallicity profile of over 800 galaxies. Reseach for this project is in collaboration with Dr. Christy Tremonti, Dr. Adam Schaefer, and Researcher Cameren Swiggum. 
                                            <br>
                                            <br>
                                            In January of 2019, I visited Seattle, Washington to attend the 223rd American Astronomical Society Meeting. I presented my research findings in collaboration with Dr. Christy Tremonti in the form of a poster presentation to both experienced and amateur astronomers.
