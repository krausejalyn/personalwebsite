									<h2>Save the Bees!</h2>
								</header>
								<section>
								    <div class="row">
                                       <p> 
                                       FACT: Bees only sting people who don't protect their ecosystems!
                                       <br>
                                       <br>
                                       Take 5 minutes to read about the bees and being a friend to all  pollinators! There are countless links throughout the report for a multitude of information and resources.
                                       </p>
								    </div>
									<div class="row">
                                       <h4>Food Sustainability Research Report completed for Environmental Studies 309, a course at UW-Madison, with Prof. Dr. Holly Gibbs in the Spring of 2019</h4>
                                       <a href="#" class="image fit"><img src="images/hive.jpg" alt=""></a>
                                        
                                        <p>
                                            Save the Bees, Please!
                                            <br>
                                            <br>
                                            The Earth’s exponential human population increase continues to put stress on global agriculture to ensure food security. “Globally, 87 of the leading 115 food crops are dependent on animal pollinators, contributing to 35% of global food production” (<a href="https://obamawhitehouse.archives.gov/the-press-office/2014/06/20/fact-sheet-economic-challenge-posed-declining-pollinator-populations">9</a>). Therefore, wild and managed bees are more crucial than ever to crop pollination for their economic and environmental services (<a href="http://www.fao.org/news/story/en/item/384726/icode/">11</a>). Furthermore, the transition to modern urban living and farming have presented many tradeoffs to Earth’s ecosystems, as we observe a decline in the global bee population due to irresponsible pesticide use and destruction of habitat through monoculture practices (<a href="https://e360.yale.edu/features/declining_bee_populations_pose_a_threat_to_global_agriculture">10</a>). I work to explore what policies have been put in place on a local and national level to protect the bee population, and what we can do to promote and practice proper beekeeping methods in Madison’s urban landscapes.
                                            <br>
                                            <br>
                                            There are over 20,000 different types of bee species worldwide, and you can find them on every continent besides Antarctica (<a href="www.nwf.org/Educational-Resources/Wildlife-Guide/Invertebrates/Bees">1</a>). The National Wildlife Federation tells us that in the US specifically, there are approximately 4,000 different native bee species occupying a variety of ecosystems from forests to deserts to grasslands (<a href="www.nwf.org/Educational-Resources/Wildlife-Guide/Invertebrates/Bees">1</a>). This diverse population of bee species can be split up into two broad groups: managed and wild. Both species provide essential ecosystem services to modern agriculture--by pollinating, in particular. There are only 46 North American bee species that are managed, but “over 90% of bee species are solitary (do not live in colonies) and wild (not managed by humans)” (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 2</a>).  The species of bees that are managed share the quality of nesting in large colonies under one queen bee making them easy for humans to regulate (<a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Gratton</a>). Ultimately, only a handful of these managed bee species have been domesticated for agricultural practices. An example of one of these species is the European honey bee (Apis mellifera), which was first introduced to the region in 1622 and is now the most familiar crop pollinator in North America (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 2</a>). Currently, honey bees enable the production of more than 90 commercially grown crops in North America (<a href="https://obamawhitehouse.archives.gov/the-press-office/2014/06/20/fact-sheet-economic-challenge-posed-declining-pollinator-populations.">9</a>). This number emphasizes how important the honey bee has become to the American food supply. Continuing on we will see that since the domestication of this species, our food production system has incredibly benefited economically and environmentally from the bees’ ecosystem services.
                                            <br>
                                            <br>
                                            It is challenging to quantify the value of pollinators in natural ecosystems, but the economic value of their services to agriculture can be made quite clear. Pollinators offer ecosystem services such as boosting yield and quality which translates to profit for the farmer (<a href="https://www.researchgate.net/publication/268521332_Where_is_the_value_in_valuing_pollination_ecosystem_services_to_agriculture">12</a>). David Kleijn and the Animal Ecology Team at the Center for Ecosystem Studies estimates that global honey bees and wild bees each contribute approximately $1200 per acre in pollination services to pollinator-dependent crops, on average (<a href="https://www.nature.com/articles/ncomms8414">5</a>). In a recent statement released from the U.S. Office of the Press Secretary, on a national level pollinators contribute more than $24 billion to the United States economy, of which honey bees account for more than $15 billion (<a href="https://obamawhitehouse.archives.gov/the-press-office/2014/06/20/fact-sheet-economic-challenge-posed-declining-pollinator-populations">9</a>). Not to mention native wild pollinators, such as the bumblebee, also contribute substantially to the domestic economy. The same statement from the White House confirms that in 2009, the crop benefits from native insect pollination in the United States were valued at more than $9 billion (<a href="https://obamawhitehouse.archives.gov/the-press-office/2014/06/20/fact-sheet-economic-challenge-posed-declining-pollinator-populations">9</a>). Furthermore, if we focus on the economic value of pollinators in Wisconsin specifically, the U.S. Department of Agriculture (USDA) reports that pollinator-dependent crops account for over $55 million in annual production (<a href="https://www.usda.gov/media/press-releases/2016/05/12/usda-releases-results-new-survey-honey-bee-colony-health">13</a>). Clearly, the economic benefits of pollinators on a global, national, and regional level cannot go unnoticed.
                                            <br>
                                            <br>
                                            But beginning in the pollinating seasons of the early 2000s, North American beekeepers noticed an increased amount of honey bee colony collapse caused by the lack of healthy adult honey bees (<a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Gratton</a>). This phenomenon is known as Colony Collapse Disorder (CCD) and it continues to affect a higher percentage of honey bee colonies today (<a href="https://obamawhitehouse.archives.gov/the-press-office/2014/06/20/fact-sheet-economic-challenge-posed-declining-pollinator-populations">9</a>). At the present time, the USDA has not reported any scientific cause for CCD. The most current research points to a complex of factors contributing to the disease, and investigators are unsure if it is a combination of all these factors or just a few that play a role in CCD (<a href=" https://www.ars.usda.gov/oc/br/ccd/index/">7</a>). Nevertheless, this condition responsible for the collapse of thousands of honey bee hives is far from the only significant threat to the health of the honey bee population and the economic stability of commercial beekeeping in the US. 
                                            <br>
                                            <br>
                                            Another significant threat is he invasive Varroa mite. “The invasive Varroa mite is one of the main culprits in pathogen-related honey bee colony loss” and explains one of the major threats to honey bee health (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 4</a>). The Varroa mite was introduced to the United States in the 1980s, and this is when the concern of pest heightened and pesticide use in beekeeping practices began (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 6</a>). Pesticides are substances meant to deter or kill organisms considered pests, including insects (insecticides), weed plants (herbicides), fungi (fungicides), mites (miticides), and others. Pesticide use and regulations are overseen by the Environmental Protection Agency (EPA), but of course, these safety guidelines are not always followed, and this has adverse effects on pollinator health and their ecosystems.
                                            <br>
                                            <br>
                                            Not to mention, modern large-scale agriculture has shifted to monoculture practices deemed more cost-effective but has done so at the expense of the pollinator’s ecosystem (Kennedy et al. <a href="https://ento.psu.edu/research/labs/john-tooker/news/2014/how-do-monocultures-influence-bee-health">2</a>). Monoculture is the planting of a single crop, livestock species, etc. in a farming system, and <a href="https://ento.psu.edu/research/labs/john-tooker">Professor John Tooker’s Lab at Penn State</a> reports that these crop plantings in agricultural environments tend to decrease local floral diversity and abundance. This, in turn, “may negatively affect local bee species populations that rely on diverse floral resources” (<a href="https://ento.psu.edu/research/labs/john-tooker">John Tooker Lab</a>). To explain, bees need two things in order to flourish in an ecosystem: a food resource and a safe space to lay eggs. Pollen is a bee’s protein source, and nectar is their sugar source (<a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Gratton</a>). Of course, suitable habitat is necessary for a bee to lay their eggs and produce offspring--ensuring the future of the species. Monoculture style farming does not always plant the crop types that bees can gather pollen from. So, when the land is cleared for these large-scale operations, pollinator ecosystems, food, and habitat are all taken away, and the new cultivation is not a suitable home for bees anymore. Therefore, to promote the bee population we must conserve, protect, and promote these two important things--pollinator habitat and food resource (<a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Gratton</a>).
                                            <br>
                                            <br>
                                            At the federal level, a <a href="https://www.whitehouse.gov/sites/whitehouse.gov/files/images/Blog/PPAP_2016.pdf">White House Pollinator Health Task Force</a> was formed, and in May of 2015, they released a national strategy for pollinator protection. In support of the national government’s efforts, a number of states are following up with plans of their own. Over the course of my research, I had the privilege of sitting down with <a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Dr. Claudio Gratton</a>, professor and researcher at the University of Wisconsin CALS Department of Entomology, who has been studying bees and colony collapse for almost 6 years. In 2015, Dr. Gratton and postdoctorate researcher in the Gratton Lab, Christina Locke, were invited to partner with the Wisconsin Department of Agriculture, Trade and Consumer Protection (DATCP) in leading a broad array of stakeholders to create Wisconsin’s first pollinator protection plan. The team worked to construct a detailed document that provides the best management practice recommendations and educational materials for beekeepers, growers, pesticide users, homeowners and landowners who want to improve the health and habitat of managed and wild pollinators. The length of <a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">this document</a> reminds us of the countless benefits available to both people and pollinators by adopting these practices recommended by the state of Wisconsin.
                                            <br>
                                            <br>
                                            Furthermore, the plan emphasizes the many actions landowners, gardeners, pesticide applicators, and beekeepers can take that make a positive difference locally and collectively on the bee population. For example, land-use practices that increase pollinator diversity can result in better pollination of garden plants and farm crops, and providing habitat and mitigating pesticide effects can bolster bees’ immune systems so that they are better able to handle other causes of compromised health--such as various diseases (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 10</a>). I am interested in uncovering what we can do as urban residents to promote the bee population locally in an effort to bring awareness to the necessity for their conservation and protection on a national and global scale in agricultural practices. 
                                            <br>
                                            <br>
                                            A simple but effective solution posed by the team to promote the bee population in urban areas is to merely plant more bee-friendly flowering plants wherever possible (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 12</a>). By building this concrete jungle around us, we took away the natural landscape and ecosystem of the pollinator in the process. Now, our land use is key to support the food source and habitat of bee species. As you recall, earlier we discussed the importance of nectar and pollen as the protein and sugar sources for a bee’s diet. By adopting the simple practice of planting more flowering plants in our urban landscapes we are providing pollinators with an abundance of food sources, therefore promoting the health and well-being of the populations in our community.
                                            <br>
                                            <a href="#" class="image fit"><img src="images/fig1.jpg" alt=""></a>
                                            
                                            <br>
                                            <a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">The Wisconsin Pollinator Protection Plan</a> lists numerous techniques to promote the bee’s habitat--the other essential element to strengthen the species population. They emphasize the fact that small-bodied bees may only travel 200 yards or less from their nests, so it is crucial that the nesting habitat is located near pollinator-attractive flowers in urban areas (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 14</a>). Typically beekeepers provide honey bee colonies a home in hive boxes, but all the other bee species found in Wisconsin nest in the wild (<a href="https://gratton.entomology.wisc.edu/people/claudio-gratton/">Gratton</a>). For instance, bumblebees are a social species and nest in small colonies, but a majority of other bee species are <a href="https://www.growwilduk.com/wildflowers/bees-pollinators/all-about-solitary-bees">solidary</a> and do not form colonies (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 2<a/>). Some tips for providing adequate nesting habitat for this type of wild bee species according to the Plan are leaving areas of debris undisturbed--if local ordinances allow--since some solitary bees species nest in the ground and in bare patches of semi-loose soil. “Bumblebees tend to nest in old rodent burrows, cavities, abandoned bird nests, and brush piles, and solitary bee species nest in hollow or pithy plant stems, downed logs, leaf litter, or old beetle holes” (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 14</a>). Additionally, planting native bunchgrasses such as little bluestem can promote bee habitat in urban landscapes, since bumblebees and solitary bee species are found to nest at the bases of these grasses (<a href="https://datcp.wi.gov/Documents/PPPComplete.pdf">6, 13</a>).
                                            <br>
                                            <br>
                                            Overall, we must comprehend the importance of promoting, protecting, and conserving the bee species--today more than ever. The invaluable ecosystem services bees provide to modern agriculture ensure global food security as the human population continues to increase. By focusing our studies on understanding how land-use and land-cover interact with the management of pollinator ecosystems, we can develop ways to enhance our food supply chain on a global, national, and regional scale. I recommend we take advantage of the resources made available to us by our state and national government and adopt practices suggested by experts to ensure the protection and conservation of the species. 
                                            <br>
                                            <br>
                                            And of course, save the bees, please!
